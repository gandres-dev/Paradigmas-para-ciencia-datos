# El tipo de estructura de datos para almecenar varios datos con los vectores
# Vectors are one-dimension arrays that can hold numeric data, character data, or logical data. 
numeric_vector <- c(2, 4, 5)
character_vector <- c("apples", "oranges", "bananas")
logical_vector <- c(TRUE, FALSE, FALSE, TRUE)

print(numeric_vector)
print(character_vector)
print(logical_vector)


